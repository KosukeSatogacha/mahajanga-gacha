"use strict";

/* =====================================================================
 * 【サイト共通設定】初心者向け編集ガイド
 * ---------------------------------------------------------------------
 * X（旧Twitter）への投稿で使う設定をここにまとめています。
 *
 * ■ 公開サイトのURLを変えたいとき
 *   → productionUrl を、実際に公開しているURLに書き換えてください。
 *     （末尾のスラッシュ "/" まで含めて入れるのがおすすめです）
 *
 * ■ 投稿に付くハッシュタグを変えたいとき
 *   → hashtags の配列を書き換えてください。先頭の "#" は付けません。
 *     例) ["旧マジャンガ州カード", "旧マジャンガ州ガチャ"]
 *
 * ※ ここを空文字 "" にすると、ローカル確認時(localhost等)では
 *   URLを付けずに投稿します（localhostのURLは投稿に入りません）。
 *
 * ■ サイトの紹介文（subtitle / introText）
 *   → 企画の説明文。トップ画面のヘッダー（index.htmlのhero）と同じ文言です。
 *     観光企画のような言い回しは避け、記録の収集企画として書きます。
 * ===================================================================== */

/**
 * @typedef {Object} SiteConfig
 * @property {string} productionUrl 公開しているガチャサイトのURL（投稿に含める）
 * @property {string[]} hashtags    投稿に付けるハッシュタグ（"#"は不要）
 * @property {string} subtitle      サイトのサブタイトル（企画の一言説明）
 * @property {string} introText     サイトの導入説明文
 */

/**
 * サイト共通設定。
 * @type {SiteConfig}
 */
const SITE_CONFIG = {
  // 公開サイトのURL（GitHub Pages などの本番URLを設定）
  productionUrl: "https://KosukeSatogacha.github.io/mahajanga-gacha/",

  // 投稿に付けるハッシュタグ（先頭の "#" は不要）
  hashtags: ["旧マジャンガ州カード", "旧マジャンガ州ガチャ"],

  // サブタイトル（index.htmlのヘッダーと同じ文言）
  subtitle: "旧マジャンガ州に関する記録をカードで集めよう",

  // 導入説明文（index.htmlのヘッダーと同じ文言）
  introText:
    "旧マジャンガ州に関する場所、人、文化、研究の記録をカードとして収集できるガチャです。1日1回カードを引いて、コレクションの完成を目指しましょう。",
};
